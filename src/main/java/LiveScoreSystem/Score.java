package LiveScoreSystem;

public class Score {
  int runs;
  int wickets;
  double overs;
  int sixes;
  int fours;

  public int getRuns() {
    return runs;
  }

  public void setRuns(int runs) {
    this.runs = runs;
  }

  public int getWickets() {
    return wickets;
  }

  public void setWickets(int wickets) {
    this.wickets = wickets;
  }

  public double getOvers() {
    return overs;
  }

  public void setOvers(double overs) {
    this.overs = overs;
  }

  public int getSixes() {
    return sixes;
  }

  public void setSixes(int sixes) {
    this.sixes = sixes;
  }

  public int getFours() {
    return fours;
  }

  public void setFours(int fours) {
    this.fours = fours;
  }
}
